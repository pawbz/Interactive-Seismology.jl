# get inverse operator using SVD (damped least squares)
# also returns the singular values for viz.
# commented for now
function get_inverse_operator(G)
    U, S, V = svd(G)
    Gi = V * Diagonal(inv.(S .+ 1e-1)) * U'
    return Gi, S
end

