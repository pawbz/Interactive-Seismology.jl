# ‚ïî‚ïê‚ï° ff0744cf-c4a7-4430-b3ee-b04c96a64dcf
md"""
The Jacobian matrix in two dimensions is given by:

```math
J = \begin{bmatrix}
\frac{\partial u_x}{\partial x} & \frac{\partial u_x}{\partial y} \\
\frac{\partial u_y}{\partial x} & \frac{\partial u_y}{\partial y}
\end{bmatrix}
```

|  | |
|:--------:|:-------:|
|$\frac{\partial u_x}{\partial x}$ = $(@bind u11 Scrubbable(-1:0.1:1, default=0.9)) | $\frac{\partial u_x}{\partial y}$= $(@bind u12 Scrubbable(-1:0.1:1))|
| $\frac{\partial u_y}{\partial x}$ = $(@bind u21 Scrubbable(-1:0.1:1, default=0.4)) | $\frac{\partial u_y}{\partial y}$ =  $(@bind u22 Scrubbable(-1:0.1:1))|



where $u_x$ and $u_y$ are components of displacements. 
* Change the elements of the Jacobian matrix to deform an infinitesimal square element below. 
* Furthermore, by Toeplitz decomposition, we can decompose the Jacobian as a sum of symmetric (strain) and skew-symmetric (rotation) matrices.
"""

# ‚ïî‚ïê‚ï° 968b1426-fca1-4d58-86cc-080a7c75e174
Jinput = [u11 u12; u21 u22]

# ‚ïî‚ïê‚ï° 174e1f52-bf7a-4201-b579-c784115d15f1
md"""
## Displacement
Particle displacement is a function of space and time
```math
ùêÆ = ùêÆ(ùê±, t).
```
Here, ùê± denotes the position the particle occupies at a reference time t=0, which means, a particle at position ùê± will be moved to position ùêÆ(ùê±, t) at time t. The particle velocity and particle acceleration are given by 
‚àÇ‚ÇúùêÆ and ‚àÇ‚Çú¬≤ùêÆ, respectively.
"""

# ‚ïî‚ïê‚ï° 94c2d557-68f3-4c36-85b0-5428e8b217dc
@variables x‚ÇÅ, x‚ÇÇ, x‚ÇÉ

# ‚ïî‚ïê‚ï° e8049366-ef8a-4adb-8b3b-d5d94b6f6475
ùê± = [x‚ÇÅ, x‚ÇÇ, x‚ÇÉ]

# ‚ïî‚ïê‚ï° a03858ff-f63f-459d-a463-702ccffe78d9
@syms u‚ÇÅ(x‚ÇÅ, x‚ÇÇ, x‚ÇÉ) u‚ÇÇ(x‚ÇÅ, x‚ÇÇ, x‚ÇÉ) u‚ÇÉ(x‚ÇÅ, x‚ÇÇ, x‚ÇÉ)

# ‚ïî‚ïê‚ï° 26d75f4e-d4b3-4254-809e-a3b5e37ebadd
ùêÆ = [u‚ÇÅ(x‚ÇÅ, x‚ÇÇ, x‚ÇÉ), u‚ÇÇ(x‚ÇÅ, x‚ÇÇ, x‚ÇÉ), u‚ÇÉ(x‚ÇÅ, x‚ÇÇ, x‚ÇÉ)]

# ‚ïî‚ïê‚ï° b2cf2af1-564e-4a3f-a846-a50042883a7a
md"""
## Distortion
Distortion of the medium is liable to change in the relative position of the particles. In other words, they are related to the gradients of ùêÆ.
"""

# ‚ïî‚ïê‚ï° 7c32c7a3-4bb5-4654-aa19-8ceea607e984
‚àá = Symbolics.Differential.(ùê±)

# ‚ïî‚ïê‚ï° 56657c39-cb42-4529-a8a8-bb58eceffff6
@variables Œ¥x‚ÇÅ, Œ¥x‚ÇÇ, Œ¥x‚ÇÉ

# ‚ïî‚ïê‚ï° cfaf2ea9-c227-470b-9ea6-fe429d656b22
Œ¥ùê± = [Œ¥x‚ÇÅ, Œ¥x‚ÇÇ, Œ¥x‚ÇÉ]

# ‚ïî‚ïê‚ï° 49d5162f-22ea-412f-b3fa-fec8a9c5a005
@einsum Œ¥ùêÆ[i] := ‚àá[j](ùêÆ[i]) * Œ¥ùê±[j]

# ‚ïî‚ïê‚ï° 4c496bae-7d72-48cd-8f24-c3dde9e16a3f
ùêÆ + Œ¥ùêÆ

# ‚ïî‚ïê‚ï° a660a926-0835-4ed2-a2a6-615e3dba3088
md"""
## Strain Tensor
We use a strain tensor to analyze the distortion of the medium, whether it is solid or fluid, elastic or inelastic.
"""

# ‚ïî‚ïê‚ï° 7bb1db67-5401-43f4-aff8-fcc00a9040b3
Markdown.MD(Markdown.Admonition("warning", "Strain Rates",
    [md"""
* Geodetic measurements in Southern California indicate a relative plate motion is $2$ to $7$ cm per year. This translates to a strain rate of approximately $3\times10^{-7}$ per year, compared to *strain accumulation* in the plate interiors that is less than $3\times 10^{-8}$ per year (an order of magnitude less).
*  During large earthquakes, the shear strain change (coseismic strain drop) is in the order of $3\times 10^{-4}$.
    """]))

# ‚ïî‚ïê‚ï° 832f75d1-64b6-4ed8-bdd9-94cbf29e42a2
Markdown.MD(Markdown.Admonition("wg", "Basic Long-term Earthquake Prediction",
    [md"""
* Division of coseismic strain drop by strain rate gives roughly the repeat times of major earthquakes, i.e., $100-1000$ years on plate boundaries, and $1000-10000$ years within plates.
    """]))

# ‚ïî‚ïê‚ï° 1ea16be6-34be-41a3-aa06-2d12e3e8d8c7
J = [‚àá[j](ùêÆ[i]) for i in 1:3, j in 1:3]

# ‚ïî‚ïê‚ï° 0ed4d8c3-b0e0-48ac-aab8-1ed956ed03cd
Jt = [‚àá[i](ùêÆ[j]) for i in 1:3, j in 1:3]

# ‚ïî‚ïê‚ï° 06be73a7-f185-4e72-816c-79cf56b59f30
md"Let's construct the strain tensor (symmetric part via Teoplitz decomposition)"

# ‚ïî‚ïê‚ï° 7fac2221-2a36-4a4c-9151-1b7c3c59914f
e = 0.5 * (J + Jt)

# ‚ïî‚ïê‚ï° a13ff96e-1885-40e4-88ed-cb5dcbad22ee
md"residual spin tensor (anti-symmetric)"

# ‚ïî‚ïê‚ï° 1d3716a1-fe01-4cd5-bb1d-88aa3c2710b5
Œ© = J - e

# ‚ïî‚ïê‚ï° 731e1777-eb47-4180-86f4-faf31e8e6cd2
e * Œ¥ùê±

# ‚ïî‚ïê‚ï° 7cabd325-30d7-4882-86c5-3195ac496264
Œ© * Œ¥ùê±

# ‚ïî‚ïê‚ï° d8f1ceea-1968-4e59-93ae-a21f035b2a09
md"## Principal Strains"

# ‚ïî‚ïê‚ï° 6400c97b-9f07-4f52-9002-e8b5f11aa182
md"## Dilatation"

# ‚ïî‚ïê‚ï° 4ca30efc-0f95-4fba-b0ae-139b21d9820d
@syms je1 je2 je

# ‚ïî‚ïê‚ï° 7f00862f-64f8-4c42-9d78-a466cd5bd9c1
Je = [je1 je; je je2]

# ‚ïî‚ïê‚ï° 451c250d-1d40-49d5-a508-4668b5cfaaad
@syms jom

# ‚ïî‚ïê‚ï° a759772e-a5b4-4f35-9150-c17627b58c4a
Jom = [0 jom; -jom 0]

# ‚ïî‚ïê‚ï° 668a0698-cfe3-4027-b190-17d0ec0366af
corners = [[0, 0], [1, 0], [1, 1], [0, 1]]

# ‚ïî‚ïê‚ï° 0ebff5dc-4ee6-4480-a546-6ca0cc99ca39
new_corners1 = map(corners) do c
    Je * c + c
end

# ‚ïî‚ïê‚ï° aeb75efa-10e0-4a6b-b24e-63a09f252d22
new_corners2 = map(corners) do c
    Jom * c + c
end

# ‚ïî‚ïê‚ï° 0861fe54-b9c4-4d00-a5f0-ebd60e87f671
function calculate_area(corner_vectors)
    A = corner_vectors[1]
    B = corner_vectors[2]
    C = corner_vectors[3]
    D = corner_vectors[4]

    area = 0.5 * abs((B[1] - A[1]) * (C[2] - A[2]) - (C[1] - A[1]) * (B[2] - A[2]) +
                     (C[1] - A[1]) * (D[2] - A[2]) - (D[1] - A[1]) * (C[2] - A[2]) +
                     (D[1] - A[1]) * (B[2] - A[2]) - (B[1] - A[1]) * (D[2] - A[2]))
end

# ‚ïî‚ïê‚ï° 9eeeac07-c2f6-4759-a328-4dee9d576723
calculate_area(new_corners1) |> simplify

# ‚ïî‚ïê‚ï° 21aa8907-b2f2-4ee6-9080-4299d1d2ce78
calculate_area(new_corners2) |> simplify

# ‚ïî‚ïê‚ï° ca079784-f5ac-4e9b-9d10-8501564c47f0
md"## UI and Plotting"

# ‚ïî‚ïê‚ï° ff59b945-f8e1-4c33-b9de-08375c0a9309
einput = 0.5 .* (Jinput + transpose(Jinput))

# ‚ïî‚ïê‚ï° 182b4b24-ee3c-44a2-b799-28bb2bd5a511
eeig = eigen(einput)

# ‚ïî‚ïê‚ï° 3ad58d32-285c-4a78-b19a-f25747de4cfa
Œ©input = Jinput .- einput

# ‚ïî‚ïê‚ï° 2a1d6186-532f-4648-bd7c-171e8e6242b5
md"""
We shall now define the locations of a bunch of points on the square element ($x‚àà[0,1]$, $y‚àà[0,1]$) at the reference time $t=t_0$. Our objective is to understand how this square element is deformed by the action of the Jacobian. We do that by looking at the displacements of these reference locations.
"""

# ‚ïî‚ïê‚ï° c974c4e2-676c-49e2-8031-7c67cded9cea
points = [[x, y] for x in range(0, stop=1, length=10), y in range(0, stop=1, length=10)];

# ‚ïî‚ïê‚ï° 07845efc-0ac1-49a5-9714-8c77c4d93089
md"""
Let us define the functions that we will be using in this notebook.
"""

# ‚ïî‚ïê‚ï° 962355bc-2e2e-4bba-b293-aefdca8f3627
"""
Function to visualize the node points. Inputs are the locations of the points (a) and title (t, optional)
"""
function plotpoints(a, t="")
    p = scatter(first.(a), last.(a), c=:red, xlim=(-2, 4), ylim=(-2, 4), label=nothing, size=(500, 500), title=t)
    return p
end

# ‚ïî‚ïê‚ï° 742fa93d-3c09-4ac3-827a-89e1ee8880cf
"""
Function to compute new locations by calculating the displacements given the reference locations (a) and the Jacobian (J).
"""
function displace(a, J)
    return [aa .+ J * aa for aa in a]
end

# ‚ïî‚ïê‚ï° e7309331-ec59-4da5-92e3-223f79136b78
begin
    Jinput
    plot1 = plotpoints(points, "Before Deformation")
    plot2 = plotpoints(displace(points, Jinput), "Total Deformation")
    plot3 = plotpoints(displace(points, einput), "Strain Deformation")
    plot!(plot3, eeig.values[1] * vcat([0.0], first(selectdim(eeig.vectors, 2, 1))), eeig.values[1] * vcat([0.0], last(selectdim(eeig.vectors, 2, 1))), c=:blue, w=2, label=nothing)
    plot!(plot3, eeig.values[2] * vcat([0.0], first(selectdim(eeig.vectors, 2, 2))), vcat([0.0], eeig.values[2] * last(selectdim(eeig.vectors, 2, 2))), c=:blue, w=2, label="Principal Strains")
    # p=scatter(first.(a), last.(a), c=:red, xlim=(-2,4), ylim=(-2,4), label=nothing, size=(500,500), title=t)
    plot4 = plotpoints(displace(points, Œ©input), "Spin Deformation")
    plot(plot1, plot2, plot3, plot4, layout=(2, 2))
end

